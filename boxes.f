      PROGRAM BOXES
        IMPLICIT NONE
        INTEGER AF,AII,BF,BII,CF,CII
        REAL AI,BI,CI
        REAL F2M
        REAL I2CM
        REAL I2F
        REAL M2F
        PARAMETER(F2M=0.3048,I2CM=2.54,I2F=0.083333333,M2F=3.2808399)
        READ 100,AI,BI,CI
        PRINT 200,AI*BI*CI
        PRINT 210,AI*M2F*BI*M2F*CI*M2F
        READ 110,AF,AII,BF,BII,CF,CII
C        PRINT *,'AF:',AF,' AII:',AII,'BF: ',BF,' BII:',BII,
c     & 'CF:',CF,' CII:',CII
        PRINT 210,(AF+AII*I2F)*(BF+BII*I2F)*(CF+CII*I2F)
100   FORMAT(F4.2,1X,F4.2,1X,F4.2)
110   FORMAT(I2,3X,I2,7X,I2,3X,I2,7X,
     & I2,3X,I2,3X)
200   FORMAT('VOLUME IS ',F7.2,' m^3')
210   FORMAT('VOLUME IS ',F7.2,' feet^3')
      STOP
      END
