      PROGRAM COGOTO
       IMPLICIT NONE
       INTEGER N
        PRINT *,'ENTER AN INTEGER NUMBER [1,4]'
        READ *,N
        GOTO(100,200,300,400),N
        GOTO 1000
100     PRINT *,'YOU SELECTED THE 100 BRANCH'
        GOTO 1000
200     PRINT *,'YOU SELECTED THE 200 BRANCH'
        GOTO 1000
300     PRINT *,'YOU SELECTED THE 300 BRANCH'
        GOTO 1000
400     PRINT *,'YOU SELECTED THE 400 BRANCH'
        GOTO 1000
1000    CONTINUE
        PRINT *,'END OF THE PROGRAM'
        STOP
      END
