      PROGRAM TH6
        IMPLICIT NONE
        INTEGER I,J
        REAL X,Y
        READ (*,101)I,J,X,Y
        PRINT *,'I=',I,' J=',J
        PRINT *,' X=',X,' Y=',Y
        PRINT *,'I/J*Y-X=',REAL(I)/REAL(J)*Y-X
        PRINT 200,Y-I,J-X,REAL(I)/REAL(J)*Y-X,I-J
101   FORMAT(BN,I4,3X,I3,TL4,F3.1,TL10,F6.2)
200   FORMAT(1H1,2F8.1,10X,F8.2,I6)
        STOP
      END

